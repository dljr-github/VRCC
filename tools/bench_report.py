"""Render contributed STT benchmark files as markdown tables (dev tool).

Reads every ``benchmarks/*.json`` written by ``tools/bench_stt.py --export``
and renders one section per machine: model rows in registry order, WER and
speed per device. ``--write`` refreshes ``benchmarks/RESULTS.md`` (do this
in the same PR that adds your JSON); without it the markdown prints to
stdout.

Run from the repo root:
    python tools/bench_report.py [--write] [benchmarks_dir]

Dev tool only -- not packaged, no test coverage.
"""

from __future__ import annotations

import json
import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parents[1]))

from vrcc.stt.registry import WHISPER_MODELS  # noqa: E402

_HEADER = """\
# STT benchmark results by machine

Generated by `python tools/bench_report.py --write` from the JSON files in
this directory -- don't edit by hand; see [README.md](README.md) for how to
add your machine. WER is word error rate on LibriSpeech test-clean English
(lower is better) and only ranks English accuracy; latency is the median
wall-clock time to transcribe one utterance; RTF is processing time over
audio duration (0.05 = 20x real time).
"""


def _fmt(record: dict | None, key: str, spec: str, unit: str = "") -> str:
    if record is None:
        return "-"
    return format(record[key], spec) + unit


def _flag(record: dict | None) -> str:
    """Mark results that didn't run where they claim (CPU fallback)."""
    if record and record["resolved_device"] != record["device"]:
        return " \\*"
    return ""


def _ordered_models(by_model: dict) -> list[str]:
    ordered = [m for m in WHISPER_MODELS if m in by_model]
    return ordered + sorted(set(by_model) - set(ordered))  # ids gone from the registry


def _speed_table(by_model: dict) -> list[str]:
    lines = [
        "| Model | Size | WER (GPU) | WER (CPU) | GPU latency | GPU RTF "
        "| CPU latency | CPU RTF |",
        "| ----- | ---- | --------- | --------- | ----------- | ------- "
        "| ----------- | ------- |",
    ]
    for model in _ordered_models(by_model):
        gpu = by_model[model].get("cuda")
        cpu = by_model[model].get("cpu")
        size = (gpu or cpu)["size_mb"]
        lines.append(
            f"| {model} | {size} MB"
            f" | {_fmt(gpu, 'wer', '.1%')}{_flag(gpu)} | {_fmt(cpu, 'wer', '.1%')}"
            f" | {_fmt(gpu, 'latency_median_s', '.2f', ' s')} | {_fmt(gpu, 'rtf', '.3f')}"
            f" | {_fmt(cpu, 'latency_median_s', '.2f', ' s')} | {_fmt(cpu, 'rtf', '.3f')} |"
        )
    return lines


def _quality_table(speed: dict, quality: dict) -> list[str]:
    """Beam 1 against beam 5 for every model measured both ways: what the
    Quality mode buys, and what it costs, on each device."""
    lines = [
        "",
        "Quality mode (beam 5) against Speed mode (beam 1). Only whisper "
        "models have a beam to widen; Parakeet and Canary always decode "
        "greedily.",
        "",
        "| Model | Device | WER Speed | WER Quality | Latency Speed "
        "| Latency Quality |",
        "| ----- | ------ | --------- | ----------- | ------------- "
        "| --------------- |",
    ]
    rows = 0
    for model in _ordered_models(quality):
        for device in ("cuda", "cpu"):
            fast = speed.get(model, {}).get(device)
            slow = quality.get(model, {}).get(device)
            if fast is None or slow is None:
                continue
            rows += 1
            lines.append(
                f"| {model} | {device}"
                f" | {_fmt(fast, 'wer', '.1%')} | {_fmt(slow, 'wer', '.1%')}"
                f" | {_fmt(fast, 'latency_median_s', '.2f', ' s')}"
                f" | {_fmt(slow, 'latency_median_s', '.2f', ' s')} |"
            )
    return lines if rows else []


def render(path: Path) -> list[str]:
    data = json.loads(path.read_text())
    machine = data["machine"]
    gpus = ", ".join(machine.get("gpus") or []) or "no GPU"
    lines = [
        f"## {machine.get('cpu', 'unknown CPU')} / {gpus}",
        "",
        f"{machine.get('os', '')} | driver {machine.get('driver') or '-'} | "
        f"{data['dataset']} | `{path.name}`",
        "",
    ]

    speed: dict[str, dict[str, dict]] = {}
    quality: dict[str, dict[str, dict]] = {}
    for record in data["results"]:
        target = speed if record.get("beam", 1) == 1 else quality
        target.setdefault(record["model"], {})[record["device"]] = record

    lines += _speed_table(speed)
    lines += _quality_table(speed, quality)

    if any(
        r["resolved_device"] != r["device"]
        for records in speed.values()
        for r in records.values()
    ):
        lines += ["", "\\* fell back to CPU (requested device unavailable)"]
    lines.append("")
    return lines


def main() -> int:
    argv = sys.argv[1:]
    write = "--write" in argv
    argv = [a for a in argv if a != "--write"]
    bench_dir = Path(argv[0] if argv else "benchmarks")

    files = sorted(bench_dir.glob("*.json"))
    if not files:
        print(f"no submissions in {bench_dir}/")
        return 1

    lines = [_HEADER]
    for path in files:
        lines += render(path)
    text = "\n".join(lines).rstrip() + "\n"

    if write:
        target = bench_dir / "RESULTS.md"
        target.write_text(text, encoding="utf-8")
        print(f"wrote {target} ({len(files)} machines)")
    else:
        print(text)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
